package es.bsc.inb.limtox.model;

public class EtoxSENDTerm {

	private String keyword;
	
	private String label;
	
	private String codelist_id;
	
	private String keyword_type;
	
	private String term_name;
	
	private String synonym_type;
	
	private String source;

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public String getCodelist_id() {
		return codelist_id;
	}

	public void setCodelist_id(String codelist_id) {
		this.codelist_id = codelist_id;
	}

	public String getKeyword_type() {
		return keyword_type;
	}

	public void setKeyword_type(String keyword_type) {
		this.keyword_type = keyword_type;
	}

	public String getTerm_name() {
		return term_name;
	}

	public void setTerm_name(String term_name) {
		this.term_name = term_name;
	}

	public String getSynonym_type() {
		return synonym_type;
	}

	public void setSynonym_type(String synonym_type) {
		this.synonym_type = synonym_type;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}
	
	
}
